void main() {
  var flavorIceCream = "Chocolate";
  
  switch (flavorIceCream) {
    case "Coco":
      print("Helado de Coco.");
      break;
    case "Chocolate":
      print("Helado de chocolate");
      break;
    case "Artesanal":
      print("Helado artesanal de frutos rojos");
      break;
    default:
      print("Sabor al azar.");
  }
}
