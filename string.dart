void main() {
  String curso = "Tecnólogo en Desarrollo de Software - ADSO";
  
  // Obtener el carácter en la posición 5
  print("Carácter en la posición 5: ${curso[5]}");
  
  // Imprimir el tamaño de la cadena
  print("Tamaño de la cadena: ${curso.length}");
  
  // Indicar si la cadena está vacía
  print("¿La cadena está vacía?: ${curso.isEmpty}");
  
  // Convertir todo el texto en mayúsculas
  print("En mayúsculas: ${curso.toUpperCase()}");
  
  // Convertir todo el texto en minúsculas
  print("En minúsculas: ${curso.toLowerCase()}");
}